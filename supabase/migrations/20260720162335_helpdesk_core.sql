create policy profiles_select on public.profiles for select to authenticated
using (id = (select auth.uid()) or private.current_role() in ('technician', 'administrator'));
create policy profiles_update on public.profiles for update to authenticated
using (id = (select auth.uid()) or private.current_role() = 'administrator')
with check (id = (select auth.uid()) or private.current_role() = 'administrator');

create policy tickets_select on public.tickets for select to authenticated
using (requester_id = (select auth.uid()) or private.current_role() in ('technician', 'administrator'));
create policy tickets_insert on public.tickets for insert to authenticated
with check (
  requester_id = (select auth.uid())
  and (assigned_to is null or private.current_role() in ('technician', 'administrator'))
);
create policy tickets_update_staff on public.tickets for update to authenticated
using (private.current_role() in ('technician', 'administrator'))
with check (private.current_role() in ('technician', 'administrator'));
create policy tickets_delete_admin on public.tickets for delete to authenticated
using (private.current_role() = 'administrator');

create policy comments_select on public.comments for select to authenticated
using (private.can_access_ticket(ticket_id));
create policy comments_insert on public.comments for insert to authenticated
with check (author_id = (select auth.uid()) and private.can_access_ticket(ticket_id));
create policy comments_update_own on public.comments for update to authenticated
using (author_id = (select auth.uid()))
with check (author_id = (select auth.uid()));
create policy comments_delete_own_or_admin on public.comments for delete to authenticated
using (author_id = (select auth.uid()) or private.current_role() = 'administrator');

create policy history_select on public.ticket_history for select to authenticated
using (private.can_access_ticket(ticket_id));

grant select, update on public.profiles to authenticated;
grant select, insert, update, delete on public.tickets to authenticated;
grant usage, select on sequence public.tickets_ticket_number_seq to authenticated;
grant select, insert, update, delete on public.comments to authenticated;
grant select on public.ticket_history to authenticated;

